package com.anindya;

public class BitWiseOperatorDemo {

	public static void main(String[] args) {
		int a=10;
		int b=11;

		System.out.println(" a&b is : "+(a&b)); // 10 
		System.out.println(" a|b is : "+(a|b)); // 11
		System.out.println(" a^b is : "+(a^b)); // 1
		

		System.out.println(" a<<2 is : "+(a<<2)); // 40
		System.out.println(" a>>2 is : "+(a>>2)); //2

	}

}
